            return false;
        

        // Checa se o bookmark pertence ao user atual.
        $id = $this->request->getParam('pass.0');
        $value = $this->value->get($id);
        

        if($value->user_id == $user['id']) 
            return true;
        */
        return parent::isAuthorized($user);
    }
}   
